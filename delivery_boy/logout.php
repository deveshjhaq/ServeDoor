?<?php
// 1. सेशन को शुरू करें ताकि उसे एक्सेस किया जा सके
session_start();

// 2. सेशन से जुड़ी सभी जानकारी को नष्ट (destroy) करें
// यह unset करने से बेहतर है क्योंकि यह सभी वेरिएबल्स को एक साथ हटा देता है
session_destroy();

// 3. function.inc.php को include करें ताकि redirect फ़ंक्शन का उपयोग कर सकें
// सुनिश्चित करें कि पाथ सही है
include_once('../function.inc.php');

// 4. यूजर को login.php पेज पर वापस भेज दें
redirect('login.php');

// रीडायरेक्ट के बाद स्क्रिप्ट को समाप्त करना एक अच्छी आदत है
exit();
?>